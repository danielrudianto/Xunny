        </figure>
        <p>Cara desain website yang terakhir adalah melakukan pengecekan tampilan desain. Pastikan desainnya tampak bagus ketika website dibuka menggunakan perangkat mobile. Hal ini karena akhir-akhir ini trafik terbesar berasal dari ponsel.</p>
