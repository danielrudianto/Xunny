        <p>Selain itu, didukung dengan page builder Wordpress yang tersedia bersifat mobile friendly. Salah satu contohnya adalah beaver builder. Apabila mengaplikasikan plugin tersebut maka akan membuat tampilan lebih bagus di smartphone.</p>
