<p>Mencegah website terserang malware dapat dilakukan dengan berbagai cara. Oleh karena itu, Anda dianjurkan untuk mempelajari caranya karena dapat mempengaruhi performanya. </p>
<p>Apabila website sudah terserang malware maka tampilan situs web akan berubah. Hal ini juga berdampak buruk dan membuat pengunjung kabur karena merasa tidak nyaman dengan munculnya pesan-pesan negatif.</p>
<p>Selain itu, jika ada pengunjung yang mengklik iklan di dalamnya maka akan terinfeksi virus. Oleh karena itu, perlu dilakukan tindakan pencegahan agar tidak merugikan baik pengunjung maupun pemilik situs web.</p>

<h2 class='h4 mt-5'>Cara Ampuh Mencegah Website Terserang Malware</h2>
<p>Beberapa tindakan berikut perlu Anda lakukan agar mencegah adanya serangan dari malware maupun jenis virus lainnya. Dengan demikian, perangkat maupun situs web tetap aman.</p>
<ol>
    <li>
        <h3 class='h5 mt-5'>Scanning Virus</h3>
        <p>Website harus didaftarkan ke Google Webmaster. Hal ini karena di dalamnya terdapat fitur yang bisa mengecek keamanan situs. Lalu, dapat melakukan scanning malware menggunakan antivirus dari cloud hosting.</p>
    </li>
    <li>
        <h3 class='h5 mt-5'>Menyaring Informasi yang Tersedia</h3>
        <p>Informasi yang tersebar di internet sangat banyak. Oleh karena itu, Anda perlu menyaring informasi tersebut karena tidak semua bersifat terpercaya. Bahkan, jika tidak berhati-hati maka bisa saja perangkat terkena virus.</p>
    </li>
    <li>
        <h3 class='h5 mt-5'>Menggunakan Firewall</h3>
        <img src="<?= base_url() ?>/assets/blogContent/Firewall.png" class='w-100' alt='Firewall Illustration' title='Firewall Illustration'>
        <p>Cara mencegah website terserang malware selanjutnya adalah dengan memakai firewall. Meskipun aktivitas browsing akan dibatasi, namun cara tersebut efektif untuk mencegah adanya serangan dari virus sehingga harus tetap aktif.</p>
    </li>
    <li>
        <h3 class='h5 mt-5'>Lebih Berhati-hati saat Menggunakan Wifi</h3>
        <p>Apabila Anda sedang menyambungkan perangkat dengan jaringan wifi umum maka jangan pernah mengakses server. Anda sangat dianjurkan menggunakan jaringan wifi pribadi agar terhindar dari malware.</p>
    </li>
    <li>
        <h3 class='h5 mt-5'>Mengupdate Perangkat Lunak</h3>
        <img src="<?= base_url() ?>/assets/blogContent/SoftwareUpdate.png" class='w-100' alt='Update Software Illustration' title='Update Software Illustration'>
        <p>Langkah untuk mencegah website terserang malware yaitu dengan melakukan update perangkat lunak agar meningkatkan keamanan. Apalagi saat ini sistem operasi banyak yang telah dilengkapi fasilitas update otomatis.</p>
    </li>
    <li>
        <h3 class='h5 mt-5'>Melakukan Backup Data</h3>
        <p>Anda harus rajin melakukan backup data di perangkat. Salah satu contohnya yaitu dengan menggunakan fitur backup data secara online pada layanan hosting.</p>
        <p>Tujuannya adalah untuk menyelamatkan data-data penting jika suatu hari terjadi hal yang tidak Anda inginkan. Dengan demikian, Anda tetap bisa mengakses data tersebut. Misalnya dengan menyimpan di hard disk eksternal.</p>
        <p>Meskipun saat ini banyak layanan hosting yang berkembang sangat pesat dan menyediakan sistem security, namun Anda harus tetap waspada. Salah satunya dengan melakukan upaya untuk mencegah website terserang malware.</p>
        <p>Tindakan tersebut penting untuk dilakukan agar nantinya Anda tidak menyesal karena kehilangan data penting yang mendukung kelancaran bisnis. Hal ini dikarenakan Anda tidak mengetahui apa yang akan terjadi kedepannya.</p>
    </li>
    <li>
        <h3 class='h5 mt-5'>Tidak Mengunggah File yang Terkena Malware</h3>
        <p>Apabila ada file yang sudah terinfeksi virus tersebut maka jangan pernah sekali-kali berpikir untuk menguploadnya lagi. Apalagi jika Anda belum membersihkan script.</p>
        <p>Hal ini dikarenakan resikonya sangat besar dan akan merugikan diri sendiri. File tersebut biasanya masih terdeteksi adanya virus. Akibatnya google akan memblokir situs web Anda.</p>
    </li>
    <li>
        <h3 class='h5 mt-5'>Mengubah Password</h3>
        <img src="<?= base_url() ?>/assets/blogContent/Password.png" class='w-100' alt='Password Change Illustration' title='Password Change Illustration'>
        <p>Cara selanjutnya untuk mencegah website terserang malware adalah dengan mengubah password secara berkala. Hal ini berlaku baik untuk password email, akun windows, Cpanel, FTP dan lain sebagainya.</p>
    </li>
    <li>
        <h3 class='h5 mt-5'>Memasang Antispyware</h3>
        <p>Spyware adalah salah satu turunan dari adware. Pada umumnya spyware tersebut akan melakukan penjelajahan di internet dan bentuknya berupa virus. Jangan lupa untuk menggunakan antispyware agar situs lebih aman.</p>
    </li>
</ol>
